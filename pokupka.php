<p>При наведении на строку цвета в случае если на сайте имеется фотография выбранного цвета, то соответствующее фото будет показано в основном поле фотографии модели</p>
 
<div style="text-align: center;"><img src="/upload/pokupka/img002.jpg" style="border:1px solid #CCC;"  /></div>
 
<p>При выборе цвета и размера ниже отображается цена на выбранный комплект, доступная для группы пользователей, к которой Вы относитесь.</p>
 
<div style="text-align: center;"><img src="/upload/pokupka/img4.jpg" style="border:1px solid #CCC;"  /></div>
 
<br />
 
<p>Справочная информация, наличие, сделать распечатку, ссылка на страницу цветов и пряж представлены в информационном блоке.</p>
 
<div style="text-align: center;"><img src="/upload/pokupka/img5.jpg" style="border:1px solid #CCC;"  /></div>
 
<br />
 
<p>При нажатии кнопки &quot;Купить&quot; товар попадает в корзину. Корзина с указанием суммарной стоимости располагается в шапке сайта справа.</p>
 
<p>На странице корзины Вы можете пересчитать свой заказ, удалить, поменять количество товаров. Переход на страницу корзины осуществляется по клику на ссылку в малой корзине.</p>
 
