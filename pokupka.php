<h1>Как сделать покупку</h1>
 
<p>Для того, чтобы сделать покупку в нашем интернет-магазине, Вам необходимо добавить нужные товары в корзину, а затем оформить заказ.</p>
 
<p>Оформление заказа возможно только зарегистрированным пользователям (<a href="/reg/ooo.html" >зарегистрироваться</a>). Авторизоваться Вы можете в любое время, в том числе и после добавления товаров в корзину перед оформлением заказа.</p>
 
<p>Добавление товаров в корзину осуществляется со страницы карточки товара.</p>
 
